extension ColorsHelper on Color {
  Color darker([double amount = 0.1]) {
    assert(amount >= 0 && amount <= 1);

    final hsl = HSLColor.fromColor(this);
    final hslDark = hsl.withLightness((hsl.lightness - amount).clamp(0.0, 1.0));

    return hslDark.toColor();
  }

  Color createPastelColor() {
    final r = red / 255.0;
    final g = green / 255.0;
    final b = blue / 255.0;

    final pastelR = (r + 1) / 2;
    final pastelG = (g + 1) / 2;
    final pastelB = (b + 1) / 2;

    return Color.fromARGB(
      alpha,
      (pastelR * 255).round(),
      (pastelG * 255).round(),
      (pastelB * 255).round(),
    );
  }
}
